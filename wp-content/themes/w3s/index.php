<h1>HTML <span class="color_h1">Comments</span></h1>
<div class="w3-clear nextprev">
<a class="w3-left w3-btn" href="html_quotation_elements.asp">&#10094; Previous</a>
<a class="w3-right w3-btn" href="html_colors.asp">Next &#10095;</a>
