<hr>
<p class="intro">HTML comments are not displayed in the browser, but they can 
help document your HTML source code.</p>
<hr>

<h2>HTML Comment Tags</h2>
<p>You can add comments to your HTML source by using the following syntax:</p>
<div class="w3-example">
<div class="w3-code notranslate htmlHigh">
&lt;!-- Write your comments here --&gt;
</div></div>
<p>Notice that there is an exclamation point (!) in the start tag, but not in the 
end tag.</p>
<div class="w3-panel w3-note">
  <p><strong>Note:</strong> Comments are not displayed by the browser, but they can help document your HTML source code.</p>
</div>

<p>With comments you can place notifications and reminders in your HTML code:</p>
<div class="w3-example">
<h3>Example</h3>
<div class="w3-code notranslate htmlHigh">
&lt;!-- This is a comment --&gt;<br><br>&lt;p&gt;This is a paragraph.&lt;/p&gt;<br>
<br>&lt;!-- Remember to add more information here --&gt;</div>
<a target="_blank" href="tryit.asp?filename=tryhtml_comment" class="w3-btn w3-margin-bottom">Try it Yourself &raquo;</a>
</div>
<p>Comments are also great for debugging HTML, because you can 
comment out HTML lines of code, one at a time, to search for errors:</p>
<div class="w3-example">
<h3>Example</h3>
<div class="w3-code notranslate htmlHigh">
&lt;!-- Do not display this image at the moment<br>
&lt;img border=&quot;0&quot; src=&quot;pic_trulli.jpg&quot; 
alt=&quot;Trulli&quot;&gt;<br>
--&gt;
</div>
<a class="w3-btn w3-margin-bottom" href="tryit.asp?filename=tryhtml_comment_out" target="_blank">Try it Yourself &raquo;</a>
</div>
